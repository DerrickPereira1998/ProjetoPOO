    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
         try {
            int indexMat = jComboBoxMatricula.getSelectedIndex();
            if (indexMat < 0) {
                JOptionPane.showMessageDialog(rootPane, "Selecionar a matricula");
                jComboBoxMatricula.requestFocus();
                return;
            }
            Funcionario FuncSelecionado = this.listamatricula.get(indexMat);       
            Funcionario funcionario = new Funcionario();
            funcionario.setMatricula((FuncSelecionado.getMatricula()));
            funcionario.setCpf(jTextField2.getText());
            funcionario.setRg(jTextField3.getText());
            funcionario.setNome(jTextField4.getText());
            funcionario.setSalario(Float.valueOf(jTextField5.getText()));
            NegocioFuncionario negocio = new NegocioFuncionario();
            negocio.cadastrar(funcionario);
            JOptionPane.showMessageDialog(rootPane, "Funcionario cadastrado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        try {
            DadosFuncionario dados = new DadosFuncionario();
            Funcionario funcionario = new Funcionario();
            funcionario.getMatricula();
            this.listamatricula = dados.listar(funcionario);
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            for (Funcionario c : this.listamatricula) {
                modelo.addElement(c.getMatricula());
            }
            jComboBoxMatricula.setModel(modelo);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

