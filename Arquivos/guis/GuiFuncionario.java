    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:
        try {

            InterfaceFuncionario dadosFuncionario = new DadosFuncionario();
            //dados.cadastrar(av);
             this.resposta = dadosFuncionario.listar(null);

            //definindo um novo modelo
            DefaultTableModel modelo = new DefaultTableModel();
            //definindo as colunas do novo modelo
            modelo.setColumnIdentifiers(new Object[]{"Matricula" ,"CPF" , "RG", "Nome", "Salario"});

            for (Funcionario a : this.resposta) {
                modelo.addRow(new Object[]{a.getMatricula(), a.getCpf(), a.getRg(), a.getNome(), a.getSalario()});
            }
            //trocando o modelo antigo
            jTable1.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

